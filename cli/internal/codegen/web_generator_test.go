func TestWebGenerator_GenerateRoutesFile(t *testing.T) {
	tempDir := t.TempDir()
	gen := NewWebGenerator(tempDir)

	routesByFile := map[string][]routeInfo{
		"user_controller.go": {
			{
				Controller:  "UserController",
				HandlerName: "List",
				Method:      "GET",
				Path:        "/users",
			},
			{
				Controller:  "UserController",
				HandlerName: "Show",
				Method:      "GET",
				Path:        "/users/:id",
			},
		},
	}

	outputPath := filepath.Join(tempDir, "helix_web_gen.go")
	err := gen.generateRoutesFile(outputPath, routesByFile)
	assert.NoError(t, err)

	// Verify file was created
	data, err := os.ReadFile(outputPath)
	assert.NoError(t, err)
	content := string(data)

	// Verify generated code structure
	assert.Contains(t, content, "package main")
	assert.Contains(t, content, "func initGeneratedRoutes()")
	assert.Contains(t, content, "internal.GlobalRouteRegistry()")
	assert.Contains(t, content, "\"GET\"")
	assert.Contains(t, content, "\"/users\"")
	assert.Contains(t, content, "UserController")
}

